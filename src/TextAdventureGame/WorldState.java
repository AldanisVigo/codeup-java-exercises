    public void printMap(Position characterPosition){
        //Print out the world map header
        System.out.println();
        System.out.println("==================================");
        System.out.println("=========== WORLD MAP ============");
        System.out.println("==================================");
        System.out.println();

        //Print out the map legend
        System.out.println("=== Map Legend ===");
        System.out.println("LOCATION - H - Home");
        System.out.println("LOCATION - P - Plaza");
        System.out.println("LOCATION - M - Market");
        System.out.println("LOCATION - K - Park");
        System.out.println("LOCATION - S - Solemn Center");
        if(characterPosition != null){ //If there's a character position available
            System.out.printf("LOCATION - # - Your Character's Position (%d,%c) OR (%d,%d)\n", characterPosition.getRow(), characterPosition.getColumn(),characterPosition.getY(), characterPosition.getX());
        }

        System.out.println();

        //Print the column numbers indicators
        System.out.print("      ");
        for(int c = 0; c < this.worldMap[0].length; c++){
            System.out.print(Position.columnsMap.get(c));
        }
        System.out.print("\n      ");
        for(int c = 0; c < this.worldMap[0].length; c++){
            System.out.print('|');
        }
        System.out.print('\n');
        //Print the map
        for(int row = 0; row < this.worldMap.length; row++){
            System.out.printf("%-2d -- ",row); //Print the row number
            for(int col = 0; col < this.worldMap[0].length; col++){
                char toPrint = ' ';
                if(this.worldMap[row][col] == WorldLocations.Home.ordinal()){
                    toPrint = 'H';
                }else if(this.worldMap[row][col] == WorldLocations.Plaza.ordinal()){
                    toPrint = 'P';
                }else if(this.worldMap[row][col] == WorldLocations.Market.ordinal()){
                    toPrint = 'M';
                }else if(this.worldMap[row][col] == WorldLocations.Park.ordinal()){
                    toPrint = 'K';
                }else if(this.worldMap[row][col] == WorldLocations.SolemnCenter.ordinal()){
                    toPrint = 'S';
                }else if(this.worldMap[row][col] == WorldLocations.Nothing.ordinal()){
                    toPrint = ' ';
                }

                if(characterPosition != null){
                    int playerX = characterPosition.getX();
                    int playerY = characterPosition.getY();
                    if((playerX == col) && (playerY == row)) {
                        toPrint = 'â˜†';
                    }
                }

                System.out.print(toPrint);
            }
            System.out.print('\n');
        }
    }

